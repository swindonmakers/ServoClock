/* Servo Layout
        0 
      1   2
        3
      4   5
        6
*/

struct CLOCKDIGIT {
    // Which character this is - eg "a", "1"
    char digit;
    // Servo positions
    // Each bit represents the position of servos
    // in order 0123456x with "x" being spare
    char positions;
};

